/** Single problem statement for Priority Actions Center. */
export function getPriorityActionProblem(lead: IntelligenceLead, action: RecommendedActionLabel): string {
    const deal = getLeadDealContext(lead.leadSlug);
    if (deal?.pendingPaymentLakhs) {
        return `₹${deal.pendingPaymentLakhs}L payment pending on booking`;
    }
    const idle = daysSinceActivity(getLastActivityDate(lead));
    if (idle >= 30) return `Lead inactive for ${idle} days`;
    if (lead.visitRecommendation && !lead.activity.some((a) => a.type === 'Meeting')) {
        return 'Site visit pending';
    }
    if (
        (action === 'Send pricing sheet' || lead.nextAction === 'Offer') &&
        !lead.activity.some((a) => a.type === 'Email')
    ) {
        return 'Proposal not sent';
    }
    if (idle >= 7) return `No follow-up for ${idle} days`;
    if (isLeadLeakage(lead)) return getRiskReason(lead);
    if (lead.followUpRisk === 'High') return 'High follow-up risk';
    return getRiskReason(lead);
}

