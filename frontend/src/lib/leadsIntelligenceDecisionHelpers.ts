export function getAutoGeneratedTasks(leads: IntelligenceLead[], limit = 6): AutoGeneratedTask[] {
    const tasks: AutoGeneratedTask[] = [];
    for (const lead of getAiActionCenterRows(leads, limit)) {
        const action = getRecommendedActionLabel(lead);
        let task = 'Call customer';
        if (action === 'Schedule site visit') task = 'Arrange site visit';
        else if (action === 'Send pricing sheet') task = 'Send quotation';
        else if (action === 'Send payment reminder') task = 'Send payment reminder';
        else if (action === 'Escalate to manager') task = 'Escalate to manager';
        else if (action === 'Nurture with content') task = 'Send nurture email';
        tasks.push({
            id: `task-${lead.id}`,
            leadId: lead.id,
            leadSlug: lead.leadSlug,
            leadName: lead.name,
            task,
            dueLabel: lead.bestCallTimeLabel !== '—' ? lead.bestCallTimeLabel : 'Today',
            priority: getFollowUpPriority(lead),
        });
    }
    return tasks;
}

