function activePipeline(leads: IntelligenceLead[]): IntelligenceLead[] {
    return leads.filter((l) => l.status !== 'Converted' && l.status !== 'Lost');
}

function addDays(iso: string, days: number): string {
    const d = new Date(iso);
    d.setDate(d.getDate() + days);
    return d.toISOString().slice(0, 10);
}

export function dateRangeForLeadsIntelPreset(preset: Exclude<LeadsIntelDatePreset, 'all' | 'custom'>): { dateFrom: string; dateTo: string } {
    const now = new Date();
    const iso = (d: Date) => d.toISOString().slice(0, 10);
    if (preset === 'today') {
        const t = iso(now);
        return { dateFrom: t, dateTo: t };
    }
    if (preset === 'week') {
        const start = new Date(now);
        start.setDate(now.getDate() - now.getDay());
        const end = new Date(start);
        end.setDate(start.getDate() + 6);
        return { dateFrom: iso(start), dateTo: iso(end) };
    }
    const start = new Date(now.getFullYear(), now.getMonth(), 1);
    const end = new Date(now.getFullYear(), now.getMonth() + 1, 0);
    return { dateFrom: iso(start), dateTo: iso(end) };
}

export function buildLeadsIntelligenceFunnel(leads: IntelligenceLead[]): LeadsIntelFunnelStep[] {
    const total = leads.length;
    const qualified = leads.filter((l) => l.status === 'Qualified' || l.status === 'Converted').length;
    const visits = leads.filter((l) => l.activity.some((a) => a.type === 'Meeting')).length;
    const bookings = leads.filter((l) => l.status === 'Converted').length;

    const pct = (num: number, den: number) => (den > 0 ? Math.round((num / den) * 100) : 0);

    const qualifiedRate = pct(qualified, total);
    const visitRate = qualified > 0 ? pct(visits, qualified) : pct(visits, total);
    const bookingRate = visits > 0 ? pct(bookings, visits) : qualified > 0 ? pct(bookings, qualified) : pct(bookings, total);

    return [
        { key: 'total', label: 'Total Leads', count: total, conversionFromPrev: null },
        { key: 'qualified', label: 'Qualified Leads', count: qualified, conversionFromPrev: qualifiedRate },
        { key: 'visits', label: 'Site Visits', count: visits, conversionFromPrev: visitRate },
        { key: 'bookings', label: 'Bookings', count: bookings, conversionFromPrev: bookingRate },
    ];
}

/** Pipeline health 0–100 from funnel step-through and leakage. */
export function getPipelineHealthScore(leads: IntelligenceLead[]): number {
    const steps = buildLeadsIntelligenceFunnel(leads);
    const rates = steps.map((s) => s.conversionFromPrev).filter((v): v is number => v != null);
    const avgStep = rates.length ? rates.reduce((a, b) => a + b, 0) / rates.length : 0;
    const leakagePenalty = leads.length ? (leads.filter(isLeadLeakage).length / leads.length) * 28 : 0;
    return Math.round(Math.min(98, Math.max(22, avgStep * 0.75 + 18 - leakagePenalty)));
}

export function getPotentialClosuresThisWeek(leads: IntelligenceLead[]): IntelligenceLead[] {
    return [...activePipeline(leads)]
        .filter((l) => l.conversionProbability >= 70 && l.temperature !== 'Cold')
        .sort((a, b) => b.conversionProbability - a.conversionProbability)
        .slice(0, 6);
}

export function getRevenueOpportunityInsights(leads: IntelligenceLead[], limit = 4): RevenueOpportunityInsight[] {
    const insights: RevenueOpportunityInsight[] = [];
    const closers = getPotentialClosuresThisWeek(leads);
    for (const l of closers.slice(0, 2)) {
        insights.push({
            id: `close-${l.id}`,
            title: 'Likely to close this week',
            detail: `${l.name} · ${l.conversionProbability}% booking probability`,
            impactLakhs: getLeadRevenuePotentialLakhs(l),
            leadSlug: l.leadSlug,
        });
    }
    const upsell = [...leads]
        .filter((l) => l.status === 'Converted' && l.leadScore >= 85)
        .slice(0, 1);
    for (const l of upsell) {
        insights.push({
            id: `upsell-${l.id}`,
            title: 'Upsell opportunity',
            detail: `Expand unit or parking with ${l.name}`,
            impactLakhs: Math.round(getLeadRevenuePotentialLakhs(l) * 0.35),
            leadSlug: l.leadSlug,
        });
    }
    const cross = [...activePipeline(leads)]
        .filter((l) => l.source === 'Referral' && l.leadScore >= 75)
        .sort((a, b) => b.leadScore - a.leadScore)[0];
    if (cross) {
        insights.push({
            id: `cross-${cross.id}`,
            title: 'Cross-sell — referral network',
            detail: `Introduce ${cross.projectInterest} to referral contacts`,
            impactLakhs: Math.round(getLeadRevenuePotentialLakhs(cross) * 0.25),
            leadSlug: cross.leadSlug,
        });
    }
    const highValue = [...activePipeline(leads)]
        .filter((l) => getLeadRevenuePotentialLakhs(l) >= 40)
        .sort((a, b) => getLeadRevenuePotentialLakhs(b) - getLeadRevenuePotentialLakhs(a))[0];
    if (highValue && insights.length < limit) {
        insights.push({
            id: `hv-${highValue.id}`,
            title: 'High-value opportunity',
            detail: `${highValue.name} · ${formatProject(highValue)}`,
            impactLakhs: getLeadRevenuePotentialLakhs(highValue),
            leadSlug: highValue.leadSlug,
        });
    }
    return insights.slice(0, limit);
}

function formatProject(lead: IntelligenceLead): string {
    return lead.projectInterest && lead.projectInterest !== '—' ? lead.projectInterest : 'General inventory';
}

export function getAiActionCenterRows(leads: IntelligenceLead[], limit = 8): IntelligenceLead[] {
    return [...activePipeline(leads)].sort((a, b) => getActionPriorityScore(b) - getActionPriorityScore(a)).slice(0, limit);
}

export function getFollowUpOpportunityRows(leads: IntelligenceLead[], limit = 6): FollowUpPlannerRow[] {
    const today = new Date().toISOString().slice(0, 10);
    return [...activePipeline(leads)]
        .filter((l) => l.bestCallTimeLabel && l.bestCallTimeLabel !== '—')
        .sort((a, b) => getActionPriorityScore(b) - getActionPriorityScore(a))
        .slice(0, limit)
        .map((lead) => {
            const priority = getFollowUpPriority(lead);
            const daysOut = priority === 'High' ? 0 : priority === 'Medium' ? 1 : 2;
            const last = lead.activity.length ? lead.activity[lead.activity.length - 1].date : lead.createdAt;
            return {
                lead,
                suggestedDate: addDays(last > today ? last : today, daysOut),
                priority,
                channel: getSuggestedFollowUpChannel(lead),
                reason:
                    lead.followUpRisk === 'High'
                        ? 'Overdue follow-up — risk of lead loss'
                        : lead.visitRecommendation
                          ? 'Site visit window — improves booking rate'
                          : 'Planned nurture touchpoint',
            };
        });
}

